package Modelo;

/**
 *
 * @author Alvaro Andres
 */
public class EntidadPadreFamilia extends Persona
{
    private int id;
    private String TelefonoPadre;
    private String EmailPadre;
    private String CiudadRecidencia;
    private String DireccionRecidencia;
    private String NombrePadre;
    private String ApellidoPadre;
    private String TipoDocumentoPadre;
    private String DocumentoPadre;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTelefonoPadre() {
        return TelefonoPadre;
    }

    public void setTelefonoPadre(String TelefonoPadre) {
        this.TelefonoPadre = TelefonoPadre;
    }

    public String getEmailPadre() {
        return EmailPadre;
    }

    public void setEmailPadre(String EmailPadre) {
        this.EmailPadre = EmailPadre;
    }

    public String getCiudadRecidencia() {
        return CiudadRecidencia;
    }

    public void setCiudadRecidencia(String CiudadRecidencia) {
        this.CiudadRecidencia = CiudadRecidencia;
    }

    public String getDireccionRecidencia() {
        return DireccionRecidencia;
    }

    public void setDireccionRecidencia(String DireccionRecidencia) {
        this.DireccionRecidencia = DireccionRecidencia;
    }

    public String getNombrePadre() {
        return NombrePadre;
    }

    public void setNombrePadre(String NombrePadre) {
        this.NombrePadre = NombrePadre;
    }

    public String getApellidoPadre() {
        return ApellidoPadre;
    }

    public void setApellidoPadre(String ApellidoPadre) {
        this.ApellidoPadre = ApellidoPadre;
    }

    public String getTipoDocumentoPadre() {
        return TipoDocumentoPadre;
    }

    public void setTipoDocumentoPadre(String TipoDocumentoPadre) {
        this.TipoDocumentoPadre = TipoDocumentoPadre;
    }

    public String getDocumentoPadre() {
        return DocumentoPadre;
    }

    public void setDocumentoPadre(String DocumentoPadre) {
        this.DocumentoPadre = DocumentoPadre;
    }
    
    
    
}
